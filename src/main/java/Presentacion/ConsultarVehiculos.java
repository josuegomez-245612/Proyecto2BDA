    private void txtFieldNumSerieKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFieldNumSerieKeyTyped
        txtFieldNumSerie.setText(txtFieldNumSerie.getText().trim());
        if (txtFieldNumSerie.getText().equals("Ingrese su numero de Serie")) {
            txtFieldNumSerie.setText("");
        }

        // Permitir solo letras y nÃºmeros
        char c = evt.getKeyChar();
        if (!((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') || (c >= '0' && c <= '9'))) {
            evt.consume();
        }
    }//GEN-LAST:event_txtFieldNumSerieKeyTyped

